/// Converter de Celsius para Fahrenheit

void main() {
  // 1. Criar uma variável para guardar a temperatura em Celsius
  var cel;

  cel = 32;

  // 2. Aplicar a fórmula de conversão salvando o resultado numa variável
  var far = ((9 * cel) + 160) / 5;

  // 3. Exibir o resultado na tela
  print("$far° F");
}
